public class Rob {
    public static int rob(int[] nums) {
        int prevMax = 0;
        int currMax = 0;
        for (int x : nums) {
            int temp = currMax;
            currMax = Math.max(prevMax + x, currMax);
            prevMax = temp;
        }
        return currMax;
    }

    public static int[] subBytes(int start, int end, int[] nums) {
        int[] newNums = new int[end - start + 1];
        for (int j = 0; j < newNums.length; j++) {
            newNums[j] = nums[j + start];
        }
        return newNums;
    }

    public static int rob2(int[] nums) {
        if (nums.length == 0){
            return 0;
        }
        if (nums.length == 1){
            return nums[0];
        }
        return Math.max(rob(subBytes(0, nums.length - 1, nums)), rob(subBytes(1, nums.length - 2, nums)));
    }
}
